// aritimeticos

console.log(2+2);
console.log(2-2);
console.log(2*2);
console.log(2/2);

// comparacao
// igual
console.log(2 == 2);
console.log(2 == 3);
console.log(2 == "2"); // retorna true porque os valores sao iguais independente do tipo

// nao igual
console.log(2 != 1);

// igual estrito
console.log(3 === "3"); // retorna false porque os tipos dos valores sao diferentes

// nao igual estrito
console.log(3 !== "3")  // retorna true porque os tipos sao diferentes

// maior que ou maior igual
console.log(2>5);
console.log(2>=5);

// menor que ou menor igual
console.log(2<5);
console.log(2<=5);